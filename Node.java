
public class Node {
	
	// Attributes 
	String Label;
	int Value;
	
	// Method getter
	public String getLabel()
	{
		return Label;
	}
	public int getValue()
	{
		return Value;
	}
	
	// Method setter
	void setLabel(String Label)
	{
		this.Label = Label;
	}
	void setValue(int Value)
	{
		this.Value = Value;
	}
	
	
}
